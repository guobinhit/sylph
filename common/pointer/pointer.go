package pointer

// Int returns int pointer.
func Int(i int) *int {
	return &i
}

// Int8 returns int8 pointer.
func Int8(i int8) *int8 {
	return &i
}

// Int16 returns int16 pointer.
func Int16(i int16) *int16 {
	return &i
}

// Int32 returns int32 pointer.
func Int32(i int32) *int32 {
	return &i
}

// Int64 returns int64 pointer.
func Int64(i int64) *int64 {
	return &i
}

// Float32 returns float32 pointer.
func Float32(f float32) *float32 {
	return &f
}

// Float64 returns float64 pointer.
func Float64(f float64) *float64 {
	return &f
}

// Bool returns bool pointer.
func Bool(b bool) *bool {
	return &b
}

// String returns string pointer.
func String(s string) *string {
	return &s
}
